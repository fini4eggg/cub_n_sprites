#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *a;
	char ch;

	a = (char *)s;
	ch = (char)c;
	while (*a != '\0')
	{
		if (*a == ch)
			return (a);
		a++;
	}
	return ((*a == ch) ? a : NULL);
}
